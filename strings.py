"""Модуль для работы со строками"""

def concatenate(str1, str2):
    """Объединение двух строк"""
    return str1 + str2

def uppercase(text):
    """Преобразование строки в верхний регистр"""
    return text.upper()

def reverse_string(text):
    """Переворачивает строку"""
    return text[::-1]